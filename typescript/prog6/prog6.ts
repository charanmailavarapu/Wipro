function sum(a : number, b: number) : number {
    return a + b;
}

function sub(a : number, b: number) : number {
    return a - b;
}

function mul(a : number, b: number) : number {
    return a * b;
}

console.log("Sum is: " + sum(10,5));
console.log("Sub is: " + sub(10,5));
console.log("Mul is: " + mul(10,5));